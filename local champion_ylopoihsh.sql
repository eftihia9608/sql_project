CREATE TABLE `Διαιτητής` (
	`ΑΜΚΑ` INT NOT NULL AUTO_INCREMENT,
	`Όνομα` VARCHAR(255),
	`Επώνυμο` VARCHAR(255),
	`Ηλικία` FLOAT NOT NULL,
	`Τύπος διαιτητή` VARCHAR(255) NOT NULL,
	PRIMARY KEY (`ΑΜΚΑ`)
);

CREATE TABLE `διαιτητεύει` (
	`id_αγώνα` FLOAT NOT NULL,
	`ΑΜΚΑ_διαιτητή` INT NOT NULL,
	PRIMARY KEY (`id_αγώνα`,`ΑΜΚΑ_διαιτητή`)
);

CREATE TABLE `Αγώνας` (
	`id_αγώνα` FLOAT NOT NULL,
	`Τοποθεσία` VARCHAR(255) NOT NULL,
	`Ώρα` TIME NOT NULL,
	`Ημερομηνία` DATE NOT NULL,
	`Νίκη` VARCHAR(255) ,
	`Ήττα` VARCHAR(255) ,
	`Γηπεδούχος` varchar(255) NOT NULL,
	`Φιλοξενούμενος` varchar(255) NOT NULL,
	`Σκορ` varchar(255) ,
	PRIMARY KEY (`id_αγώνα`)
);

CREATE TABLE `παίζει` (
	`id_αγώνα` FLOAT NOT NULL,
	`id_ομάδας` FLOAT NOT NULL,
	PRIMARY KEY (`id_αγώνα`,`id_ομάδας`)
);

CREATE TABLE `Ομάδα` (
	`id_ομάδας` FLOAT NOT NULL AUTO_INCREMENT,
	`Έδρα` VARCHAR(255) NOT NULL,
	`Αριθμός αγώνων` FLOAT NOT NULL,
	`Αριθμός νικών` FLOAT NOT NULL,
	`Όνομα` VARCHAR(255) NOT NULL,
	PRIMARY KEY (`id_ομάδας`)
);

CREATE TABLE `προπονείται` (
	`id_ομάδας` FLOAT NOT NULL,
	`ΑΜΚΑ_προπονητή` FLOAT NOT NULL,
	PRIMARY KEY (`id_ομάδας`,`ΑΜΚΑ_προπονητή`)
);

CREATE TABLE `Προπονητής` (
	`Όνομα` VARCHAR(255) NOT NULL,
	`Επώνυμο` VARCHAR(255) NOT NULL,
	`ΑΜΚΑ` FLOAT NOT NULL AUTO_INCREMENT,
	`Χρόνια εμπειρίας` FLOAT NOT NULL,
	`Ηλικία` FLOAT NOT NULL,
	PRIMARY KEY (`ΑΜΚΑ`)
);

CREATE TABLE `έχει` (
	`id_ομάδας` FLOAT NOT NULL,
	`ΑΜΚΑ_παίκτη` FLOAT NOT NULL,
	PRIMARY KEY (`id_ομάδας`,`ΑΜΚΑ_παίκτη`)
);

CREATE TABLE `Παίκτης` (
	`ΑΜΚΑ` FLOAT NOT NULL AUTO_INCREMENT,
	`Όνομα` VARCHAR(255) NOT NULL,
	`Επώνυμο` VARCHAR(255) NOT NULL,
	`Ηλικία` FLOAT NOT NULL,
	`Θέση` VARCHAR(255) NOT NULL,
	`Αριθμός` INT NOT NULL,
	`Κλεψίματα` FLOAT ,
	`Ασίστ` FLOAT ,
	`Rebound` FLOAT,
	`Πόντοι` FLOAT ,
	`Μπλοκ` FLOAT,
	`Χρόνος που αγωνίζεται` TIME,
	`Βάρος` FLOAT NOT NULL,
	`Ύψος` FLOAT NOT NULL,
	PRIMARY KEY (`ΑΜΚΑ`)
);
